template <class TSample>
Neurowave<TSample>::Neurowave()
{
    wavetable_.assign(600, (TSample)0.0);
}

template <class TSample>
inline void Neurowave<TSample>::generate_wave(const bool &audio_range)
{
    for (auto s = 0; s < 600; s++)
    {
        double output = biases_[s];

        for (auto l = 0; l < 8; l++)
        {
            output += latent_.at(l) * weights_[l][s];
        }

        output = std::exp(output) / (1.0 + std::exp(output));

        if (audio_range)
        {
            output *= 2.0;
            output -= 1.0;
        }

        wavetable_.at(s) = hann((TSample)s / (TSample)599) * (TSample)output;
    }
}

template <class TSample>
void Neurowave<TSample>::set_latent_parameter(const double &value, const int &index)
{
    if (index >= 0 && index < 8)
    {
        latent_.at(index) = value;
    }
}

template <class TSample>
void Neurowave<TSample>::set_latent_space(const std::array<double, 8> &latent_space)
{
    for (auto l = 0; l < 8; l++)
    {
        set_latent_parameter(latent_space.at(l), l);
    }
}

template <class TSample>
std::vector<TSample> Neurowave<TSample>::get_wavetable()
{
    return wavetable_;
}

template <class TSample>
std::array<TSample, 8> Neurowave<TSample>::get_latent_space()
{
    return latent_;
}

